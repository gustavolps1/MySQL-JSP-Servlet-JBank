package jbank;

public class Banco {

	private String num_Banco;
	private String nome;

	public String getId_Banco() {
		return num_Banco;
	}

	public void setId_Banco(String id_Banco) {
		this.num_Banco = id_Banco;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
